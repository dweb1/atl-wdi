function sumArray(input) {
  var total = 0;
  for (var i = 0; i < input.length; i++) {
    total += input[i];
  } return total;
}

// Question 4
function multiplyArray(input) {
  var total = 1;
  for (var i = 0; i < input.length; i++) {
    total = total * input[i];
  } return total;
}

